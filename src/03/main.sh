#!/bin/bash

echo ""


if [[ $1 =~ ^[0-6]$ ]] && [[ $2 =~ ^[0-6]$ ]] && [[ $3 =~ ^[0-6]$ ]] && [[ $4 =~ ^[0-6]$ ]]; then

    if [ "$1" -ne "$2" ] && [ "$3" -ne "$4" ]; then
        . colorizer.sh
        . screen_echo.sh
    else
        echo -n "Цвета шрифта и фона одного столбца не должны совпадать. Необходимо вызвать скрипт повторно."
    fi

else
  echo "Введены не корректные параметры"
fi


echo ""